    {
        comment::create([
            'post_id' => $request->post_id,
            'name' => $request->name,
            'email'=> $request->email,
            'comment' => $request->message,
            'created_at' => date('Y-m-d H:i:s'),
        ]);
        return redirect()->back()->with('success', 'Komentar berhasil ditambahkan!');
